public class VariablesAndDataTypes {
    public static void main(String[] args) {
        int age = 25;
        double height = 5.9;
        char gender = 'M';
        String name = "John Doe";
        
        System.out.println(name + " is " + age + " years old, " + height + " feet tall, and of gender " + gender);
    }
}
